void SYS_INIT(u32 clock){
		
    SYSTICK->STCTRL =0x0;
	SYSTICK->STRELOAD =clock-1;                    
	SYSTICK->STCURRENT=0x0;
	SYSTICK->STCTRL =0x07;
	
}

void INCREMENT(void) {
	SYSTEM_MS++;
}

u32 millis(void){
	return SYSTEM_MS;
}
void system_msdelay(u32 delay_ms){
	
	u32 start = millis() ;
	while((millis()-start)< delay_ms);

}

void Register_cb(cb_type ptr){

if(0!=ptr){
	CallBackPtr = ptr;
}

}

void SysTick_Handler(void){
if(0!= CallBackPtr){
CallBackPtr();
	}
}

